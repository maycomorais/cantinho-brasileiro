function iniciarTimerAutoConfirmacao(pedidoId) {
    // 4 horas em milissegundos
    const QUATRO_HORAS = 4 * 60 * 60 * 1000;
    
    // Cancela timer anterior se existir
    if (autoConfirmTimer) {
        clearTimeout(autoConfirmTimer);
    }
    
    // Inicia novo timer
    autoConfirmTimer = setTimeout(async () => {
        console.log('â° 4 horas passadas, confirmando entrega automaticamente...');
        await confirmarEntregaAutomatica(pedidoId);
    }, QUATRO_HORAS);
    
    // Salva timestamp no localStorage para persistir entre reloads
    const agora = new Date().getTime();
    const tempoExpiracao = agora + QUATRO_HORAS;
    localStorage.setItem('cantinho_confirmExpiry_' + pedidoId, tempoExpiracao);
    
    console.log('â° Timer de auto-confirmaÃ§Ã£o iniciado para 4 horas');
}

// ===== FUNÃ‡ÃƒO PARA RESTAURAR TIMER APÃ“S RELOAD =====
function restaurarTimerSeNecessario() {
    const pedidoId = localStorage.getItem('cantinho_pedido_id');
    if (!pedidoId) return;
    
    const tempoExpiracao = localStorage.getItem('cantinho_confirmExpiry_' + pedidoId);
    if (!tempoExpiracao) return;
    
    const agora = new Date().getTime();
    const tempoRestante = parseInt(tempoExpiracao) - agora;
    
    if (tempoRestante > 0) {
        // Ainda hÃ¡ tempo restante
        console.log('â° Restaurando timer de auto-confirmaÃ§Ã£o...');
        autoConfirmTimer = setTimeout(async () => {
            await confirmarEntregaAutomatica(pedidoId);
        }, tempoRestante);
    } else {
        // Tempo jÃ¡ expirou, confirmar agora
        console.log('â° Tempo expirado, confirmando agora...');
        confirmarEntregaAutomatica(parseInt(pedidoId));
    }
}

// ===== CONFIRMAÃ‡ÃƒO AUTOMÃTICA (4 HORAS) =====
async function confirmarEntregaAutomatica(pedidoId) {
    try {
        const { error } = await supa
            .from('pedidos')
            .update({ 
                status: 'entregue',
                tempo_entregue: new Date().toISOString()
            })
            .eq('id', parseInt(pedidoId));  // parseInt garante que nÃ£o Ã© string
        
        if (error) throw error;
        
        console.log('âœ… Entrega confirmada automaticamente apÃ³s 4 horas');
        
        // Limpa dados locais
        localStorage.removeItem('cantinho_confirmExpiry_' + pedidoId);
        fecharTracker();
        
        // Mostra notificaÃ§Ã£o
        if (Notification.permission === 'granted') {
            new Notification('Pedido Entregue âœ…', {
                body: 'Sua entrega foi confirmada automaticamente. Obrigado!'
            });
        }
    } catch (err) {
        console.error('Erro ao confirmar entrega automÃ¡tica:', err);
    }
}

// ===== CONFIRMAÃ‡ÃƒO MANUAL (CLIENTE) =====
async function confirmarEntregaCliente() {
    const pedidoId = localStorage.getItem('cantinho_pedido_id');
    if (!pedidoId) {
        alert('Erro: Pedido nÃ£o encontrado');
        return;
    }
    
    if (!confirm('Confirmar que vocÃª recebeu o pedido?')) {
        return;
    }
    
    try {
        const { error } = await supa
            .from('pedidos')
            .update({ 
                status: 'entregue',
                tempo_entregue: new Date().toISOString()
            })
            .eq('id', parseInt(pedidoId));  // parseInt garante tipo correto
        
        if (error) throw error;
        
        console.log('âœ… Entrega confirmada pelo cliente');
        
        // Cancela timer automÃ¡tico
        if (autoConfirmTimer) {
            clearTimeout(autoConfirmTimer);
        }
        localStorage.removeItem('cantinho_confirmExpiry_' + pedidoId);
        
        // Atualiza UI
        mostrarMensagemEntregaConfirmada();
        
        // Fecha tracker apÃ³s 3 segundos
        setTimeout(() => {
            fecharTracker();
        }, 3000);
        
    } catch (err) {
        console.error('Erro ao confirmar entrega:', err);
        alert('Erro ao confirmar entrega. Tente novamente.');
    }
}

