async function aplicarCupom() {
    const codigo = document.getElementById('cupom-codigo')?.value?.trim().toUpperCase();
    const msgBox = document.getElementById('cupom-msg');
    
    if (!codigo) {
        msgBox.innerHTML = '<span style="color:#e74c3c">Digite um cÃ³digo</span>';
        msgBox.style.display = 'block';
        return;
    }
    
    // Busca no banco
    const { data: cupom, error } = await supa
        .from('cupons')
        .select('*')
        .eq('codigo', codigo)
        .eq('ativo', true)
        .single();
    
    if (error || !cupom) {
        msgBox.innerHTML = '<span style="color:#e74c3c">âŒ Cupom invÃ¡lido ou inativo</span>';
        msgBox.style.display = 'block';
        cupomAplicado = null;
        atualizarTotalCheckout();
        return;
    }

    // Verifica validade
    if (cupom.validade) {
        const vDate = new Date(cupom.validade + 'T23:59:59');
        if (vDate < new Date()) {
            msgBox.innerHTML = '<span style="color:#e74c3c">âŒ Cupom expirado</span>';
            msgBox.style.display = 'block';
            cupomAplicado = null;
            atualizarTotalCheckout();
            return;
        }
    }

    // Verifica limite de usos
    if (cupom.limite_uso && cupom.limite_uso > 0) {
        const usados = cupom.usos_realizados || 0;
        if (usados >= cupom.limite_uso) {
            msgBox.innerHTML = `<span style="color:#e74c3c">âŒ Este cupom atingiu o limite de ${cupom.limite_uso} usos</span>`;
            msgBox.style.display = 'block';
            cupomAplicado = null;
            atualizarTotalCheckout();
            return;
        }
    }
    
    const subtotal = carrinho.reduce((a, i) => a + i.preco * i.qtd, 0);
    if (subtotal < cupom.minimo) {
        msgBox.innerHTML = `<span style="color:#e74c3c">Valor mÃ­nimo: Gs ${cupom.minimo.toLocaleString('es-PY')}</span>`;
        msgBox.style.display = 'block';
        cupomAplicado = null;
    } else {
        cupomAplicado = cupom;
        const restante = cupom.limite_uso
            ? ` (${cupom.limite_uso - (cupom.usos_realizados || 0)} restantes)`
            : '';
        msgBox.innerHTML = `<span style="color:#27ae60">âœ… Cupom aplicado!${restante}</span>`;
        msgBox.style.display = 'block';
    }
    
    atualizarTotalCheckout();
}
