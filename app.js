function salvarCarrinhoLocal() {
  try {
    if (carrinho && carrinho.length > 0) {
      localStorage.setItem('cantinho_carrinho_backup', JSON.stringify(carrinho));
      localStorage.setItem('cantinho_carrinho_backup_time', new Date().toISOString());
    } else {
      localStorage.removeItem('cantinho_carrinho_backup');
      localStorage.removeItem('cantinho_carrinho_backup_time');
    }
  } catch (e) {
    console.warn('NÃ£o foi possÃ­vel salvar backup do carrinho:', e);
  }
}

function restaurarCarrinhoBackup() {
  try {
    const backup = localStorage.getItem('cantinho_carrinho_backup');
    const backupTime = localStorage.getItem('cantinho_carrinho_backup_time');

    if (backup && backupTime) {
      const tempoBackup = new Date(backupTime);
      const agora = new Date();
      const diffHoras = (agora - tempoBackup) / (1000 * 60 * 60);

      // SÃ³ restaura se o backup tiver menos de 24 horas
      if (diffHoras < 24) {
        const carrinhoSalvo = JSON.parse(backup);
        if (carrinhoSalvo && carrinhoSalvo.length > 0) {
          if (confirm('VocÃª tem itens no carrinho de uma sessÃ£o anterior. Deseja restaurÃ¡-los?')) {
            carrinho = carrinhoSalvo;
            updateUI();
            mostrarToast('âœ… Carrinho restaurado!', 'success');
          }
        }
      }
    }
  } catch (e) {
    console.warn('NÃ£o foi possÃ­vel restaurar backup do carrinho:', e);
  }
}

